// Función para convertir las expresiones en formato de juego
const createExpressionObject = (expression, category = 'everyday') => {
  // Dividir la expresión en palabras
  const words = expression.split(' ');

  // Si la expresión tiene solo una palabra, no la usamos para el juego
  if (words.length <= 1) return null;

  // Seleccionar una palabra aleatoria para ocultar (evitando artículos y preposiciones cortas)
  const avoidWords = ['a', 'an', 'the', 'to', 'of', 'in', 'on', 'at', 'by', 'for', 'with', 'and', 'or', 'but', 'is', 'am', 'are'];
  const eligibleWords = words.filter(word =>
    word.length > 2 && !avoidWords.includes(word.toLowerCase())
  );

  // Si no hay palabras elegibles, no usamos esta expresión
  if (eligibleWords.length === 0) return null;

  // Seleccionar una palabra aleatoria para ocultar
  const wordToHide = eligibleWords[Math.floor(Math.random() * eligibleWords.length)];
  const wordIndex = words.indexOf(wordToHide);

  // Crear la expresión con el espacio en blanco
  const textWithBlank = words.map((word, index) =>
    index === wordIndex ? '_____' : word
  ).join(' ');

  return {
    text: textWithBlank,
    answer: wordToHide,
    category: category,
    translation: expression,
    explanation: `Expresión común en inglés: "${expression}"`
  };
};

